import math

def visualize_detections(pred_results, max_show=12, cols=4):
    detections = [(r, r.orig_img) for r in pred_results if len(r.boxes) > 0]
    no_det     = [(r, r.orig_img) for r in pred_results if len(r.boxes) == 0]

    # Prioritize images with detections
    show_list = (detections + no_det)[:max_show]
    rows      = math.ceil(len(show_list) / cols)

    fig, axes = plt.subplots(rows, cols, figsize=(cols*4, rows*3.2))
    fig.patch.set_facecolor('#0d1117')
    axes = axes.flatten() if rows > 1 or cols > 1 else [axes]

    for idx, (r, img) in enumerate(show_list):
        ax = axes[idx]
        ax.set_facecolor('#0d1117')

        # Show original image (no annotation overlay yet)
        rgb = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)
        ax.imshow(rgb)

        # Draw each detection box manually
        for box in r.boxes:
            x1, y1, x2, y2 = box.xyxy[0].cpu().numpy().astype(int)
            conf = box.conf.item()
            cls  = int(box.cls.item())

            rect = patches.Rectangle(
                (x1, y1), x2-x1, y2-y1,
                linewidth=2.5, edgecolor='#00ff88', facecolor='none'
            )
            ax.add_patch(rect)
            label = f"silver_lining {conf:.0%}"
            ax.text(x1+2, y1-5, label, color='black', fontsize=7.5, fontweight='bold',
                    bbox=dict(boxstyle='round,pad=0.15',
                              facecolor='#00ff88', edgecolor='none', alpha=0.95))

