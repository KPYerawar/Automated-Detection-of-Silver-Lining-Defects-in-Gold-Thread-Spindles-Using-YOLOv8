import time

def benchmark_model(model_path, source_dir, n_runs=3, is_onnx=False):
    m = YOLO(model_path)
    times = []
    imgs_to_benchmark = [os.path.join(source_dir, f) for f in os.listdir(source_dir)
             if f.lower().endswith(IMAGE_EXTENSIONS)][:5]

    if not imgs_to_benchmark:
        print(f"Warning: No valid images found in {source_dir} for benchmarking.")
        return 0.0, 0.0

    for _ in range(n_runs):
        t0 = time.time()
        if is_onnx:
            # For ONNX models exported with dynamic=False, predict one image at a time
            for img_path in imgs_to_benchmark:
                m.predict(source=img_path, verbose=False, save=False)
        else:
            # For PyTorch models, it's efficient to predict all at once
            m.predict(source=imgs_to_benchmark, verbose=False, save=False)
        times.append((time.time() - t0) / len(imgs_to_benchmark) * 1000)
    return np.mean(times), np.std(times)

