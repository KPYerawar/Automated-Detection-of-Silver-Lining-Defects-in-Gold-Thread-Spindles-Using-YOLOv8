# VGG16 + Logistic Regression for spindle silver lining classification

import torch
import torch.nn as nn
from torchvision import models, transforms
from torch.utils.data import DataLoader, Dataset
from PIL import Image
import os
import numpy as np
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import classification_report, confusion_matrix
import matplotlib.pyplot as plt
import seaborn as sns

# 1. Dataset Class (reads your folder structure)
class SpindleDataset(Dataset):
    def __init__(self, root_dir, transform=None):
        self.root_dir = root_dir
        self.transform = transform
        self.classes = ['good_threads', 'silver_linings']
        self.samples = []

        for idx, class_name in enumerate(self.classes):
            class_dir = os.path.join(root_dir, class_name)
            for img_name in os.listdir(class_dir):
                if img_name.lower().endswith(('.jpg', '.jpeg', '.png')):
                    self.samples.append((os.path.join(class_dir, img_name), idx))

    def __len__(self):
        return len(self.samples)

    def __getitem__(self, idx):
        img_path, label = self.samples[idx]
        image = Image.open(img_path).convert('RGB')
        if self.transform:
            image = self.transform(image)
        return image, label

# 2. Transforms (ImageNet standards)
transform = transforms.Compose([
    transforms.Resize((224, 224)),
    transforms.ToTensor(),
    transforms.Normalize(mean=[0.485, 0.456, 0.406],
                        std=[0.229, 0.224, 0.225])
])

# 3. Load YOUR dataset
data_root = '/content/drive/MyDrive/Colab spindle_project /spindle_dataset'  # Your unzipped path
dataset = SpindleDataset(root_dir=data_root, transform=transform)

print(f" Dataset loaded: {len(dataset)} total images")
print(f"Classes: good_threads={sum(1 for _,l in dataset.samples if l==0)}, silver_linings={sum(1 for _,l in dataset.samples if l==1)}")

