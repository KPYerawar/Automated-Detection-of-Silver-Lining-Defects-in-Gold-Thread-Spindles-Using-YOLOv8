# 4. Train/test split (80/20)
train_size = int(0.8 * len(dataset))
test_size = len(dataset) - train_size
train_dataset, test_dataset = torch.utils.data.random_split(dataset, [train_size, test_size])

train_loader = DataLoader(train_dataset, batch_size=32, shuffle=True)
test_loader = DataLoader(test_dataset, batch_size=32, shuffle=False)

print(f"Train: {len(train_dataset)}, Test: {len(test_dataset)}")

# 5. Load VGG16 PRE-TRAINED (freeze conv layers)
print("\n Loading VGG16...")
vgg16 = models.vgg16(pretrained=True)
vgg16.eval()
for param in vgg16.parameters():
    param.requires_grad = False

# 6. Extract features (paper method)
def extract_features(loader, model):
    model.eval()
    features, labels = [], []
    with torch.no_grad():
        for images, targets in loader:
            feats = model.features(images).view(images.size(0), -1)
            features.append(feats.cpu().numpy())
            labels.append(targets.numpy())
    return np.vstack(features), np.hstack(labels)

print("Extracting features...")
train_features, train_labels = extract_features(train_loader, vgg16)
test_features, test_labels = extract_features(test_loader, vgg16)

print(f"Features ready!")
print(f"Train: {train_features.shape}, Test: {test_features.shape}")

# 7. Train Logistic Regression CLASSIFIER
print("\n Training classifier...")
lr_classifier = LogisticRegression(max_iter=2000, random_state=42, C=1.0)
lr_classifier.fit(train_features, train_labels)

# 8. Predict & Results
test_pred = lr_classifier.predict(test_features)
accuracy = (test_pred == test_labels).mean()

print("\n" + "="*70)
print("RESULTS: VGG16 + Logistic Regression")
print("="*70)
print(f"Test Accuracy: {accuracy:.3%}")
print("\nDetailed Report:")
print(classification_report(test_labels, test_pred,
                          target_names=['Good Threads', 'Silver Linings']))

